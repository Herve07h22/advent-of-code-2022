it("A rucksack can be filled with items", () => {
  const ruckSacksCollection = fillRuckSacks(inputTest);
  expect(ruckSacksCollection.rucksacks).toHaveLength(6);
  expect(ruckSacksCollection.rucksacks[0].compartiments[0]).toBe(
    "vJrwpWtwJgWr"
  );
  expect(ruckSacksCollection.rucksacks[0].compartiments[1]).toBe(
    "hcsFMMfFFhFp"
  );
});

it("A ruck sack can tell what are the items that appear in both compartments", () => {
  const ruckSacksCollection = fillRuckSacks(inputTest);
  expect(ruckSacksCollection.rucksacks[0].commonItems()).toEqual(["p"]);
  expect(ruckSacksCollection.rucksacks[1].commonItems()).toEqual(["L"]);
});

it("We can compute the alphabetical position of a letter", () => {
  expect(alphabeticalPosition("a")).toBe(1);
  expect(alphabeticalPosition("z")).toBe(26);
  expect(alphabeticalPosition("Z")).toBe(26);
});

it("We can compute the priority of an item", () => {
  expect(computeItemPriority("a")).toBe(1);
  expect(computeItemPriority("z")).toBe(26);
  expect(computeItemPriority("A")).toBe(27);
  expect(computeItemPriority("Z")).toBe(52);
});

it("We can compute the sum of the priorities of the items that appear in both compartments", () => {
  const ruckSacksCollection = fillRuckSacks(inputTest);
  expect(ruckSacksCollection.rucksacks[0].commonItemsPrioritiesSum()).toBe(16);
  expect(ruckSacksCollection.rucksacks[1].commonItemsPrioritiesSum()).toBe(38);
});

it("We can compute the sum of the priorities of a collection of rucksacks", () => {
  const ruckSacksCollection = fillRuckSacks(inputTest);
  expect(ruckSacksCollection.commonItemsPrioritiesSum()).toBe(157);
});

